// //Strongly typed syntax

// English:
// Strong typing ensures that variables are explicitly declared with a specific data type, and type checking is
// enforced during compile-time or runtime. This helps catch type-related errors early and ensures that operations are
// performed on compatible data types.

// Urdu:
// Strong typing ye ensure karta hai ke variables ko explicitly aik specific data type ke sath declare kiya jaye, 
// or type checking compile-time ya runtime ke dauran enforce ki jaye. Yeh type se related errors ko jaldi pakarne or
// ensure karne mein madad karta hai ke operations compatible data types par perform kiye jayein.


let me: string = "shoaib";
me = "azlaan";

let you: boolean = true;
let they: number = 33;


// <----------------------------------------END---------------------------------------->
// Type inference

// English:
// Type inference automatically determines the type of a variable based on its assigned value without explicit type
// declarations This allows for more flexible and concise code, though it may lead to less type safety.

// Urdu:
// Type inference automatically variable ki type uski assigned value ke base par determine karta hai bina explicit type
// declarations ke Ye flexible or concise code likhne ki saholat deta hai lekin type safety kam ho sakti hai.


let a = "getting";
a = "loosing";

let g = false;
g = true;

let f = 9.2;
f = 777